    // REGISTER NEW USER
    [HttpPost("register")]
    public async Task<ActionResult> Register([FromBody] RegisterDto registerDto)
    {
        if (await userManager.Users.AnyAsync(x => x.Email == registerDto.Email))
        {
            return BadRequest("Email is already taken.");
        }

        var residentProfile = new Resident
        {
            FirstName = registerDto.FirstName,
            MiddleName = registerDto.MiddleName,
            LastName = registerDto.LastName,
            Purok = registerDto.Purok,
            PictureUrl = registerDto.PictureUrl,
            IsHeadOfFamily = false,
            CivilStatus = registerDto.CivilStatus,
            MonthlyIncome = 0
        };

        unit.Repository<Resident>().Add(residentProfile);

        var saved = await unit.Complete();
        if (!saved) return BadRequest("Problem creating resident profile.");

        var user = new AppUser
        {
            UserName = registerDto.Email,
            Email = registerDto.Email,
            PhoneNumber = registerDto.PhoneNumber,
            IdUrl = registerDto.IdUrl,
            IsIdVerified = false,
            ResidentId = residentProfile.Id,
            Resident = residentProfile
        };

        bool isPasswordAutoGenerated = string.IsNullOrWhiteSpace(registerDto.Password);
        string password = isPasswordAutoGenerated
            ? GenerateSecureRegistrationPassword()
            : registerDto.Password;

        var result = await userManager.CreateAsync(user, password);

        if (!result.Succeeded)
        {
            foreach (var error in result.Errors)
            {
                ModelState.AddModelError(error.Code, error.Description);
            }
            return ValidationProblem();
        }

        await userManager.AddToRoleAsync(user, "Resident");

        try
        {
            string emailBody = BuildRegistrationEmailTemplate(registerDto.FirstName, registerDto.Email, password, isPasswordAutoGenerated);
            await emailService.SendEmailAsync(user.Email, "UgnayBarangay - Account Registration Confirmation", emailBody);
        }
        catch (Exception ex)
        {
            
        }
        
        return Ok(new { message = "Registration successful. Please wait for ID verification." });
    }

